.8086
.MODEL SMALL
.DATA
ORG 100H;CS起始地址
STR DB 20,0,20 DUP(20H);字符串STR
.CODE
START:
	MOV AX,@DATA
	MOV DS,AX;数据段地址
	LEA DX,STR;将STR的相对于数据段首地址的偏移地址放进DX
	MOV AH,0AH
	INT 21H;从键盘输入一个字符串
	
	MOV DL,0AH
	MOV AH,02
	INT 21H;输出DL中的换行
	MOV DL,0DH
	MOV AH,02
	INT 21H;输出回车
	
	MOV BL,STR[1]
	MOV BH,0;BX=20
	MOV BYTE PTR STR[BX+2],'$';末尾加'$'
	LEA DX,STR+2
	MOV AH,9
	INT 21H;输出这一句话
	MOV AH,4CH
	INT 21H;返回DOS
END START
